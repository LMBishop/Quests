package me.fatpigsarefat.quests.utils.values;

public class NormalCertainValue {

	private int id;
	private int value;
	
	public NormalCertainValue(int id, int value) {
		this.id = id;
		this.value = value;
	}
	
	public int getId() {
		return id;
	}
	
	public int getValue() {
		return value;
	}
	
}
